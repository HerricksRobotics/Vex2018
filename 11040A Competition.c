task usercontrol()
{

	short leftSpeed;
	short rightSpeed;
	short armLiftCtrl;

	while(true)
	{
			/*				!!!		DRIVE CONTROLS		!!!
			*/
			//To control the left side using channel 3
			if (vexRT[Ch3] > -35 && vexRT[Ch3] < 35)
			{
				leftSpeed = 0;
			}
			else
			{
				leftSpeed = vexRT[Ch3];
			}

			//To control the right side using channel 2
			if (vexRT[Ch2] > -35 && vexRT[Ch2] < 35)
			{
				rightSpeed = 0;
			}
			else
			{
				rightSpeed = vexRT[Ch2];
			}

			leftDrive(leftSpeed);
			rightDrive(rightSpeed);



			/*				!!!		MOBILE GOAL CARRIER CONTROLS		!!!

					Pressing Button 6U should bring the mobile goal carrier up
					Pressing Button 6D should bring the mobile goal carrier down
			*/


			armLiftCtrl = (vexRT[Btn6U] << 1) + vexRT[Btn6D];
			switch (armLiftCtrl) {
				case 1:
					armDrive(127);
					break;

				case 2:
					armDrive(127);
					break;

				default:
					armDrive(0);
					break;

			}
		}
  }
